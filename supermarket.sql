-- Show all products
SELECT * FROM Products;

-- Low stock (< 50)
SELECT ProductName, Quantity FROM Products WHERE Quantity < 50;

-- Show all customers
SELECT * FROM Customers;

-- Sales report with names
SELECT s.SaleID, c.Name AS Customer, e.Name AS Employee, s.SaleDate, s.TotalAmount
FROM Sales s
JOIN Customers c ON s.CustomerID = c.CustomerID
JOIN Employees e ON s.EmployeeID = e.EmployeeID
ORDER BY s.SaleID;

-- Bill for SaleID = 1
SELECT sd.SaleID, p.ProductName, sd.Quantity, sd.Price, (sd.Quantity * sd.Price) AS Subtotal
FROM SaleDetails sd
JOIN Products p ON sd.ProductID = p.ProductID
WHERE sd.SaleID = 1;

-- Daily sales
SELECT DATE(SaleDate) AS SaleDate, SUM(TotalAmount) AS DailySales
FROM Sales
GROUP BY DATE(SaleDate)
ORDER BY SaleDate;

-- Top selling products
SELECT p.ProductName, SUM(sd.Quantity) AS TotalSold
FROM SaleDetails sd
JOIN Products p ON sd.ProductID = p.ProductID
GROUP BY p.ProductName
ORDER BY TotalSold DESC;

